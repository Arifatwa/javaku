
package nasira;

public class Nasira {
       private String nama;
       private int umur;
       private float tinggi;
       private boolean status;
       private long bulan_lahir;
       private char kelas;
       private double angkatan;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getUmur() {
        return umur;
    }

    public void setUmur(int umur) {
        this.umur = umur;
    }

    public float getTinggi() {
        return tinggi;
    }

    public void setTinggi(float tinggi) {
        this.tinggi = tinggi;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public long getBulan_lahir() {
        return bulan_lahir;
    }

    public void setBulan_lahir(long bulan_lahir) {
        this.bulan_lahir = bulan_lahir;
    }

    public char getKelas() {
        return kelas;
    }

    public void setKelas(char kelas) {
        this.kelas = kelas;
    }

    public double getAngkatan() {
        return angkatan;
    }

    public void setAngkatan(double angkatan) {
        this.angkatan = angkatan;
    }
}
    
       
        
               
             
    